    public void pegarFornecedor(String nome){
        // buscando fornecedor por nome paracadastrar seu id no banco de dados
        FornecedorController f = new FornecedorController();
        f.listarFornecedorByNome(nome);
        for (Fornecedor c : f.listarFornecedorByNome(nome)) {
           id = c;
        }
        
    }
   
