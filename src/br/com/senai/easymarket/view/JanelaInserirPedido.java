    private void botaoCadastrarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarPedidoActionPerformed
     // Setando os valores que estavam no formulário para o objeto pedido criado
        Pedido c = new Pedido();
        c.setCodigo(campoCodigo.getText());
        String fornecedor = comboFornecedor.getSelectedItem().toString();
        pegarFornecedor(fornecedor);
        c.setFornecedor(id);
        c.setValor(Double.parseDouble(campoValor.getText()));
        // formatando a data para cadastrar bo banco de dados
        try{
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");  
            java.sql.Date data = new java.sql.Date(format.parse(campoDataPedido.getText()).getTime()); 
            c.setDataPedido(data);
            java.sql.Date data2 = new java.sql.Date(format.parse(campoDataEntrega.getText()).getTime()); 
            c.setDataEntrega(data2);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro ao converter data.");
        }
        PedidoController cc = new PedidoController();
        if(cc.salvar(c)){
            dispose();
            JanelaGerenciarPedidos jc = new JanelaGerenciarPedidos();
            jc.setVisible(true);
        }
    }//GEN-LAST:event_botaoCadastrarPedidoActionPerformed

