    private void botaoCadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarClienteActionPerformed
        Produto c = new Produto();
        c.setNome(campoNome.getText());
        c.setMarca(campoMarca.getText());
        c.setCategoria(campoCategoria.getText());
        c.setCodigo(campoCodigo.getText());
        c.setValor(Double.parseDouble(campoValor.getText()));
        try{
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");  
            java.sql.Date data = new java.sql.Date(format.parse(campoValidade.getText()).getTime()); 
            c.setValidade(data);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro ao converter data.");
        }
        ProdutoController cc = new ProdutoController();
        if(cc.salvar(c)){
            dispose();
            JanelaGerenciarProdutos jc = new JanelaGerenciarProdutos();
            jc.setVisible(true);
        }
    }//GEN-LAST:event_botaoCadastrarClienteActionPerformed

